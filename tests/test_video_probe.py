"""Tests for dimljus.video.probe — requires ffprobe in PATH.

Tests are skipped automatically if ffprobe is not available.
Uses tiny test videos generated by conftest.py fixtures.
"""

from pathlib import Path

import pytest

from tests.conftest import requires_ffmpeg

from dimljus.video.errors import FFmpegNotFoundError, ProbeError
from dimljus.video.probe import (
    _parse_frame_rate,
    _parse_sar,
    probe_directory,
    probe_video,
)


# ---------------------------------------------------------------------------
# Unit tests — no ffprobe needed
# ---------------------------------------------------------------------------

class TestParseFrameRate:
    """Tests for frame rate string parsing."""

    def test_fraction(self) -> None:
        assert _parse_frame_rate("30/1") == 30.0

    def test_ntsc(self) -> None:
        """23.976 fps = 24000/1001."""
        assert abs(_parse_frame_rate("24000/1001") - 23.976) < 0.001

    def test_plain_float(self) -> None:
        assert _parse_frame_rate("16.0") == 16.0

    def test_invalid(self) -> None:
        assert _parse_frame_rate("invalid") == 0.0

    def test_zero_denominator(self) -> None:
        assert _parse_frame_rate("30/0") == 0.0


class TestParseSAR:
    """Tests for SAR string normalization."""

    def test_colon_format(self) -> None:
        assert _parse_sar("1:1") == "1:1"

    def test_slash_format(self) -> None:
        assert _parse_sar("1/1") == "1:1"

    def test_non_square(self) -> None:
        assert _parse_sar("4:3") == "4:3"

    def test_na(self) -> None:
        assert _parse_sar("N/A") == "1:1"

    def test_none(self) -> None:
        assert _parse_sar(None) == "1:1"

    def test_zero(self) -> None:
        assert _parse_sar("0:0") == "1:1"


